// Helper function to extract string path from Next.js asset imports
// Next.js may return objects with .src property or strings directly
const getAssetPath = (asset) => {
  if (!asset) return '';
  if (typeof asset === 'string') return asset;
  if (typeof asset === 'object') {
    // Handle Next.js image objects with .src property
    if (asset.src) return asset.src;
    // Handle default exports
    if (asset.default) {
      const defaultVal = asset.default;
      if (typeof defaultVal === 'string') return defaultVal;
      if (defaultVal && typeof defaultVal === 'object' && defaultVal.src) return defaultVal.src;
    }
  }
  return asset;
};

